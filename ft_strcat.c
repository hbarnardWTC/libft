#include "libft.h"

char *ft_strcat(char *restrict s1, const char *restrict s2)
{
	int j;

	j = 0;
	while (s2[j])
	{
		s1[ft_strlen(s1) + j] = s2[j];
		j++;
	}
	return (s1);
}
