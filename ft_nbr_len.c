#include "libft.h"

size_t ft_nbr_len(int n)
{
    int i;

    i = 1;
    if (n < 0)
    {
        n *= -1;
        i++;
    }
    while (n > 9)
    {
        i++;
        n = n / 10;
    }
    return (i);
}
